'''
Obliczanie pola powierzchni i obwodu koła o zadanym promieniu
'''
import math
# ustal promień koła i PI
# ... tu wpisz instrukcje programu ...
pi =  math.pi
r = 5

# oblicz pole i obwód
# ... tu wpisz instrukcje programu ...
polePowierzchni = pi*r**2
obwodKola = 2*pi*r
# wyświetl rezultaty
# ... tu wpisz instrukcje programu ...
# Pole koła o promieniu ... wynosi ...
print (f"Pole koła o promieniu {r} wynosi {polePowierzchni}")

# Obwód koła o promieniu ... wynosi ...
print (f"Obwód koła o promieniu {r} wynosi {obwodKola}")